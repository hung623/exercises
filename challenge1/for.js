for (var i = 10; i > -1; i--) {
  console.log(i)
}

// for kjører koden et par ganger, så var i har vi satt variablen til i
// i sin value er 10; og da hvis i ikke er mindre en -1; så kjører den 
// i-- som tar minus på 10 helt til den er 0

// En anne måte å gjøre løkker (repetere oppgaver), er å bruke while.
let counter = 0
while (counter < 10) {
  console.log(counter)
  counter++
}